# You can evaluate any expression in Python, and get one of two answers, True or False.
# For example
print(10 > 9) # Will result in True
print(10 == 1) # Will result in False

# Running a condition in an if statement will return either True or False:
a = "KBTU"
b = " GREAT"

if a + b == "KBTU GREAT":
  print("True")
else:
  print("False") 